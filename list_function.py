#append function

fruits = ["orange" , "mango" , "apple"]
fruits.append("banana")
print(fruits)

#length of the list

print(len(fruits))

#insert function

fruits.insert(1 , "peach")

print(fruits)

#index function

print(fruits.index("banana"))

#range function

numbers = list(range(10))
print(numbers)

numbers1 = list(range(1,50))
print(numbers1)
#specifying the interval

numbers2 = list(range(1,50,5))
print(numbers2)